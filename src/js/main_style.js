function showCategorias(){
    itemsBox = document.getElementById('categorias-box')
    itemsBox.style.display = 'block'
    selection = document.getElementById('categorias-selection')
    selection.style.backgroundColor = 'rgb(117, 117, 117)'
}
function hideCategorias(){
    itemsBox = document.getElementById('categorias-box')
    itemsBox.style.display = 'none'
    selection = document.getElementById('categorias-selection')
    selection.style.backgroundColor = 'rgb(185, 185, 185)'
}

